export function getDistrictsForIndianState(state) {
  const raw = String(state || '').trim();
  if (!raw) return [];
  if (INDIAN_DISTRICTS_BY_STATE[raw]) return INDIAN_DISTRICTS_BY_STATE[raw];
  const lower = raw.toLowerCase();
  const key = Object.keys(INDIAN_DISTRICTS_BY_STATE).find((s) => s.toLowerCase() === lower);
  return key ? INDIAN_DISTRICTS_BY_STATE[key] : [];
}

/** Match API/free-text district to a dropdown option for the given state. */
export function matchIndianDistrict(state, district) {
  const raw = String(district || '').trim();
  if (!raw) return '';
  const options = getDistrictsForIndianState(state);
  if (!options.length) return raw;
  const exact = options.find((d) => d === raw);
  if (exact) return exact;
  const lower = raw.toLowerCase();
  const ci = options.find((d) => d.toLowerCase() === lower);
  if (ci) return ci;
  const compact = lower.replace(/s+/g, '');
  const fuzzy = options.find((d) => d.toLowerCase().replace(/s+/g, '') === compact);
  if (fuzzy) return fuzzy;
  const contains = options.find((d) => {
    const dl = d.toLowerCase();
    return lower.includes(dl) || dl.includes(lower);
  });
  return contains || raw;
}
